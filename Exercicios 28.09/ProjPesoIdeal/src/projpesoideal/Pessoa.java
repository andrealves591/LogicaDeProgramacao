    public double calcularPesoIdeal(){//CRIAÇÃO DO MÉTODO calcularPesoIdeal, DO TIPO DOUBLE PORQUE VAI RETORNAR UM VALOR VÍRGULA, SEM PARÂMETROS PORQUE É DE RETORNO
        if(this.sexo==1){//SE O SEXO FOR IGUAL 1, ENTÃO, SEGUNDO A LEGENDA DA CLASSE MAIN, SERÁ CALCULADO O PESO SE A PESSOA FOR DO SEXO MASCULINO
            return (72.7*this.altura)-58;//RETORNA O VALOR DO PESO IDEAL SE A PESSOA FOR HOMEM
        }//FIM DA ESTRUTURA CONDICIONAL 
         return (62.1*this.altura)-44.7;//RETORNA O VALOR DO PESO IDEAL SE A PESSOA FOR MULHERE
    }//FIM DO MÉTODO calcularPesoIdeal
