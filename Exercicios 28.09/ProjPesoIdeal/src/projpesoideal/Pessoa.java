    private double pesoAtual;//DECLARAÇÃO DA VARIÁVEL PESO ATUAL, DO TIPO DOUBLE PORQUE É UM NÚMERO COM VÍRGULA E PRIVADO PARA QUE APENAS ESTA CLASSE TENHA ACESSO A ELA
